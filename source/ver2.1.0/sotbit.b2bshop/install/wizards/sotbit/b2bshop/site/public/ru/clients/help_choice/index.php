?><h2>Как снять мерки<br>
 </h2>
<p>
</p>
<p>
</p>
<ul>
	<li>
	<p>
 <b>Рост:</b> снимите обувь и прислонитесь к ровной вертикальной поверхности. Рост измеряется от макушки до ступней.
	</p>
 </li>
	<li>
	<p>
 <b>Обхват груди</b>: измеряется по наиболее выступающим точкам груди.
	</p>
 </li>
	<li>
	<p>
 <b>Обхват талии:</b> измеряется вокруг талии, держитесь при этом свободно и естественно.
	</p>
 </li>
	<li>
	<p>
 <b>Боковая длина брюк:</b> лучше всего снять эту мерку с брюк, которые на вас очень хорошо сидят. Измеряйте без учета пояса до нижнего края брюк. Иначе можно измерить на теле, начиная от косточки бедра до ступней.
	</p>
 </li>
	<li>
	<p>
 <b>Длина стопы:</b> встаньте обеими ногами на лист бумаги и очертите контуры стопы. Измерьте расстояние в сантиметрах.
	</p>
 </li>
</ul>
<p>
</p>
 Измерения производите в нижнем белье и так, чтобы сантиметр плотно прилегал к телу. Если вы воспользуетесь при снятии мерок чьей-нибудь помощью, замеры будут точнее.<br>
