<h2>Стандартные и нестандартные размеры<br>
 </h2>
 Стандартные размеры предназначены для мужчин, у которых грудная клетка значительно шире талии.<br>
 <br>
 Размеры для коренастых мужчин рассчитаны на невысоких, коренастых мужчин, ширина грудной клетки у которых незначительно превышает обхват талии. Грудная клетка и талия скроены в этом случае свободнее по сравнению со стандартным размером.<br>
 <br>
 Размеры для мужчин с животом предназначены для мужчин, обхват талии у которых больше грудной клетки. В данном случае изделия скроены в области грудной клетки и талии особенно широко по сравнению со стандартным размером.<br>
 <br>
 Размеры для худощавых мужчин подойдут высоким, стройным мужчинам с не очень широкими плечами. Талия при этом немного уже грудной клетки.<br>
